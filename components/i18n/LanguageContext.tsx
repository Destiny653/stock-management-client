export type Translation = typeof translations.en;
export type TranslationKey = keyof Translation;

interface LanguageContextType {
  language: string;
  setLanguage: (lang: string) => void;
  t: (key: TranslationKey, params?: Record<string, string | number>) => string;
  translations: typeof translations;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  // Always start with default language to avoid hydration mismatch
  const [language, setLanguage] = useState<string>('en');
  const [isHydrated, setIsHydrated] = useState(false);

  // Load saved language preference after hydration
  useEffect(() => {
    const savedLanguage = localStorage.getItem('app_language');
    if (savedLanguage) {
      setLanguage(savedLanguage);
    }
    setIsHydrated(true);
  }, []);

  // Save language preference when it changes (but only after hydration)
  useEffect(() => {
    if (isHydrated) {
      localStorage.setItem('app_language', language);
    }
  }, [language, isHydrated]);

  const t = (key: TranslationKey, params: Record<string, string | number> = {}) => {
    const currentLang = language as keyof typeof translations;
    // Cast to unknown then Translation to allow access, assuming structure matches enough or falls back
    const langTranslations = translations[currentLang] as unknown as Translation;
    let text = langTranslations[key] || translations['en'][key] || key;

    // Support for parameterized translations
    Object.keys(params).forEach(param => {
      text = text.replace(`{${param}}`, String(params[param]));
    });
    return text;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, translations }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

